<?php
$valor = 20;

// pré-incremento
echo ++$valor;
echo "<br>";
echo $valor;

echo "<hr>";

// pós-incremento
echo $valor++;
echo "<br>";
echo $valor;

echo "<hr>";

// pré-decremento
echo --$valor;
echo "<br>";
echo $valor;

echo "<hr>";

// pós-decremento
echo $valor--;
echo "<br>";
echo $valor;
?>